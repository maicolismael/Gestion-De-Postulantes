    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // Obtener datos de la interfaz
    String nuevoTitulo = txtTitulo.getText();
    String nuevoContenido = txtContenido.getText();
    
    // 1. Validaciones b√°sicas
    if (nuevoTitulo.trim().isEmpty() || nuevoContenido.trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Debe completar el t√≠tulo y el contenido de la noticia.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    Connection conn = null;
    PreparedStatement psDesactivar = null;
    PreparedStatement psInsertar = null;
    
    // SQL para las dos acciones
    String sqlDesactivar = "UPDATE noticias SET activo = FALSE WHERE activo = TRUE";
    String sqlInsertar = "INSERT INTO noticias (titulo, contenido,activo) VALUES (?, ?, ?)";

    try {
        // Establecer la conexi√≥n (asumiendo tu clase 'conexion' en min√∫sculas)
        conn = conexion.getConnection(); 
        
        // --- PASO 1: Desactivar la Noticia Antigua ---
        psDesactivar = conn.prepareStatement(sqlDesactivar);
        psDesactivar.executeUpdate(); // Ejecuta la desactivaci√≥n

        // --- PASO 2: Insertar la Nueva Noticia (Autom√°ticamente activa por defecto) ---
        psInsertar = conn.prepareStatement(sqlInsertar);
        psInsertar.setString(1, nuevoTitulo);
        psInsertar.setString(2, nuevoContenido);
        psInsertar.setBoolean(3, true);
        
        int filasInsertadas = psInsertar.executeUpdate();

        if (filasInsertadas > 0) {
            JOptionPane.showMessageDialog(this, "Nueva noticia publicada exitosamente. La anterior fue desactivada.", "√âxito", JOptionPane.INFORMATION_MESSAGE);
            // Opcional: Limpiar la interfaz despu√©s de la publicaci√≥n
            txtTitulo.setText("");
            txtContenido.setText("");
        }

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error de Base de Datos al publicar: " + e.getMessage(), "Error SQL", JOptionPane.ERROR_MESSAGE);
        e.printStackTrace();
    } finally {
        // Cerrar recursos en orden inverso (PreparedStatement y luego Connection)
        try {
            if (psDesactivar != null) psDesactivar.close();
            if (psInsertar != null) psInsertar.close();
        } catch (SQLException e) {
            e.printStackTrace();
        }
        // Usar tu m√©todo de clase para cerrar la conexi√≥n
        conexion.close(conn); 
    }

    }//GEN-LAST:event_jButton5ActionPerformed

