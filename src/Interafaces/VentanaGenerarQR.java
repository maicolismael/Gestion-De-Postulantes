    private void btnGenerarQRActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarQRActionPerformed
        // TODO add your handling code here:
        String datos = "Nombre: " + nombre + " " + apellidoP + " " + apellidoM + "\nMonto: " + monto;
        try {
            QRCodeWriter qr = new QRCodeWriter();
            BitMatrix bm = qr.encode(datos, BarcodeFormat.QR_CODE, 200, 200);
            Path path = Path.of("pagoQR.png");
            MatrixToImageWriter.writeToPath(bm, "PNG", path);
            lblQR.setIcon(new ImageIcon(path.toAbsolutePath().toString()));
            JOptionPane.showMessageDialog(this, "QR generado correctamente.");
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al generar QR: " + e.getMessage());
        }
    }//GEN-LAST:event_btnGenerarQRActionPerformed

    private void btnValidarPagoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnValidarPagoActionPerformed
        // TODO add your handling code here:
        try (Connection con = conexion.getConnection();
             PreparedStatement ps = con.prepareStatement(
                     "UPDATE pago SET estado='PAGADO' WHERE ci_postulante=? AND estado='PENDIENTE'")) {

            ps.setString(1, ci);
            int res = ps.executeUpdate();

            if (res > 0) {
                JOptionPane.showMessageDialog(this, "Pago validado correctamente.");
            } else {
                JOptionPane.showMessageDialog(this, "No se encontr√≥ un pago pendiente para este usuario.");
            }

        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al validar pago: " + e.getMessage());
        }

    }//GEN-LAST:event_btnValidarPagoActionPerformed

